	/**
	 * Test of menorDistanciaBellMan method, of class GrafoArrayList.
	 */
	@Test
	public void testMenorDistanciaBellMan() {
		Grafo grafo = new GrafoArrayList(true, true);
		
		grafo.adicionaVertice(42);
		grafo.adicionaVertice(20);
		grafo.adicionaVertice(60);
		grafo.adicionaVertice(80);
		grafo.adicionaVertice(90);
		
		
		grafo.adicionaAresta(42, 20, 6);
		grafo.adicionaAresta(42, 60, 7);
		grafo.adicionaAresta(20, 80, 5);
		grafo.adicionaAresta(20, 60, 8);
		grafo.adicionaAresta(20, 90, -4);
		grafo.adicionaAresta(60, 80, -3);
		grafo.adicionaAresta(60, 90, 9);
		grafo.adicionaAresta(80, 20, -2);
		grafo.adicionaAresta(90, 80, 7);
		grafo.adicionaAresta(90, 42, 2);
		
		assertEquals(0, grafo.menorDistanciaBellMan(42, 42));
		assertEquals(-2, grafo.menorDistanciaBellMan(42, 90));
		assertEquals(2, grafo.menorDistanciaBellMan(42, 20));
	}
